    private static void handelconnection() throws IOException {
        while(true){
        Socket socket = null;
        try {
            socket = serverSocket.accept();
            in = new DataInputStream(socket.getInputStream());
            out = new DataOutputStream(socket.getOutputStream());
            String username = in.readUTF();
            String pass = in.readUTF();
            msg_area.append("User Name : " + username+"\n");
            msg_area.append("Password : " + pass+"\n");
            if (checkuser(username, pass)) {
                out.writeUTF("SUCCESSFUL LOGIN");
                msg_area.append("\nSUCCESSFUL LOGIN \n"+"Ready For Echo Msgs :\n\n");
                int num_msg = 0;
                for (String msg = in.readUTF(); !msg.equals("***STOP***"); msg = in.readUTF()) {
                    msg_area.append("CLIENT>>  "+msg+"\n");
                }
            } else {
                msg_area.append("\nOPPS :( >>INVALID USERNAME OR PASSWORDS\n"+":) Try Again\n"+"**********************\n");
                out.writeUTF("\nOPPS :( >>INVALID USERNAME OR PASSWORDS\n");
            }
        }catch (IOException var16) {
            var16.printStackTrace();
        } 
        }
    }

    private static boolean checkuser(String username, String pass) {
        int pos = -1;

        for(int i = 0; i < names.length; ++i) {
            if (username.equals(names[i])) {
                pos = i;
                break;
            }
        }

        return pos > -1 && pass.equals(num[pos]);
    }
    
    
